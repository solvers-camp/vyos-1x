def multiply_numbers(a,b):
    return a*b  # Proper spacing around operators

def divide_numbers(a,b):
    return a/b  # Proper spacing around operators

def main():
    product = multiply_numbers(4,5,6)  # Proper spacing after commas
    quotient = divide_numbers(10,2)  # Proper spacing after commas
    print(f"Product: {product}, Quotient: {quotient}")

if __name__ == "__main__":
    main()
